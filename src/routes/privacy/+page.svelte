<p>We collect information you enter or upload, including:</p>
<ul>
<li>
<p>Personal details (e.g., preferences, notes, goals).</p>
</li>
<li>
<p>Additional health information you choose to include.</p>
</li>
<li>
<p>Documents or data you upload manually.</p>
</li>
</ul>
<h3><strong>1.2 EHR Information Retrieved via SMART on FHIR</strong></h3>
<p>When you authorize a SMART on FHIR connection, we retrieve whatever data your healthcare provider makes available, which may include:</p>
<ul>
<li>
<p>Demographics</p>
</li>
<li>
<p>Problem lists</p>
</li>
<li>
<p>Allergies and medications</p>
</li>
<li>
<p>Laboratory results</p>
</li>
<li>
<p>Clinical notes</p>
</li>
<li>
<p>Immunizations</p>
</li>
<li>
<p>Encounter history</p>
</li>
<li>
<p>Other FHIR resources made available by the provider</p>
</li>
</ul>
<p>We do not control what your provider chooses to share.</p>
